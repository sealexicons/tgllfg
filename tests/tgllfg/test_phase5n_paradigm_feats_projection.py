"""Phase 5n.A Commit 3 — SAY_CLASS / ASK_CLASS paradigm-engine propagation (§18 L91).

The §18.1 L91 entry described an "inconsistency in how the paradigm
engine projects ``feats:`` blocks", citing ``sabi`` (root) /
``sabihin`` as cells where SAY_CLASS=YES "fails to land", in contrast
to ``sinabi`` / ``sasabihin`` where it does. Audit (Phase 5n.A
Commit 3) showed the original characterisation was a misdiagnosis: the
projection IS uniform — every generated VERB cell of ``sabi`` carries
SAY_CLASS=YES; every generated cell of ``tanong`` carries
ASK_CLASS=YES. The cells the §18 entry called "missing" (``sabi`` bare
citation, ``sabihin`` OV-imperative, ``sabihan`` DV-imperative,
``tanong`` bare, ``tanungin`` OV-imperative) are not generated by the
paradigm engine at all — they're imperative-like surfaces that lack a
paradigm cell, not regular cells where the feat was being filtered out.

The "missing imperative cells" issue is a separate paradigm-engine gap
(potentially addressed by Phase 5n.A Commit 14's ``kainin`` paradigm
extension for L72), not a feat-projection bug.

These tests provide a comprehensive regression net: every surface the
analyzer indexes for ``sabi`` / ``tanong`` / ``payag`` / ``pilit`` /
``utos`` carries the lemma's root-level ``feats:`` block. The
projection mechanism is in
``src/tgllfg/morph/analyzer.py:_index_verb_paradigms`` (the
``for k, v in root.feats.items(): feats.setdefault(k, v)`` loop —
applied uniformly across every paradigm cell that fires for the root's
``affix_class``).
"""

from __future__ import annotations

import pytest

from tgllfg.morph import Analyzer


# === Universal SAY_CLASS / ASK_CLASS coverage =============================


class TestSayClassUniformProjection:
    """Every generated VERB surface for the ``sabi`` root carries
    SAY_CLASS=YES, regardless of voice / aspect / mood / affix-class."""

    def test_every_sabi_surface_carries_say_class(self) -> None:
        analyzer = Analyzer.from_default()
        surfaces_with_lemma_sabi: list[tuple[str, dict]] = []
        for surface, analyses in analyzer._index.verb_forms.items():
            for a in analyses:
                if a.lemma == "sabi":
                    surfaces_with_lemma_sabi.append((surface, dict(a.feats)))

        # Sanity: there should be many cells (mag-PFV/IPFV/CTPL/SOC,
        # in_oblig PFV/IPFV/CTPL, an_oblig PFV/IPFV/CTPL, maka
        # PFV/IPFV/CTPL/RECPFV).  The exact count varies with the
        # paradigm inventory; require at least 10.
        assert len(surfaces_with_lemma_sabi) >= 10, (
            f"unexpected sabi-cell coverage; got "
            f"{len(surfaces_with_lemma_sabi)} surfaces"
        )

        missing = [
            (surface, feats)
            for surface, feats in surfaces_with_lemma_sabi
            if feats.get("SAY_CLASS") != "YES"
        ]
        assert missing == [], (
            f"sabi cells missing SAY_CLASS=YES: "
            f"{[s for s, _ in missing]}"
        )


